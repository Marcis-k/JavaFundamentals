package basic;

public class CharExamples {
    public static void main(String[] args) {

        char ch = 'Q';

        System.out.println(ch);

        System.out.println(++ch);

        System.out.println((char) 106);

        for (int i=0;i<=100;i++){

            System.out.println(ch+i);
        }

    }

}
